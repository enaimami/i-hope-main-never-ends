#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;

	if (!s || !f)
		return ;
	i = 0x00;
	while (s[i] != 0x00)
	{
		f(i, s + i);
		i++;
	}
}
